function pembulatan(){
    var x = 12.73;
    console.log("pembulatan ceil : ", Math.ceil(x));    // pembulatan ke atas
    console.log("pembulatan floor : ", Math.floor(x));  // pembulatan ke bawah
    console.log("pembulatan round : ", Math.round(x));  // pembulatan tergantung 0.5-nya
}

pembulatan();